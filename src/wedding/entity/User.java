package wedding.entity;

public class User {
	private int user_id;
	private String user_img;
	private String user_nickname;
	private String user_sex;
	private String user_city;
	private String user_IDcard;
	private String user_add;
	private String user_postcode;
	private String user_QQ;
	private String user_tel;
	private String user_password;
	private String user_realname;
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getUser_img() {
		return user_img;
	}
	public void setUser_img(String user_img) {
		this.user_img = user_img;
	}
	public String getUser_nickname() {
		return user_nickname;
	}
	public void setUser_nickname(String user_nickname) {
		this.user_nickname = user_nickname;
	}
	public String getUser_sex() {
		return user_sex;
	}
	public void setUser_sex(String user_sex) {
		this.user_sex = user_sex;
	}
	public String getUser_city() {
		return user_city;
	}
	public void setUser_city(String user_city) {
		this.user_city = user_city;
	}
	public String getUser_IDcard() {
		return user_IDcard;
	}
	public void setUser_IDcard(String user_IDcard) {
		this.user_IDcard = user_IDcard;
	}
	public String getUser_add() {
		return user_add;
	}
	public void setUser_add(String user_add) {
		this.user_add = user_add;
	}
	public String getUser_postcode() {
		return user_postcode;
	}
	public void setUser_postcode(String user_postcode) {
		this.user_postcode = user_postcode;
	}
	public String getUser_QQ() {
		return user_QQ;
	}
	public void setUser_QQ(String user_QQ) {
		this.user_QQ = user_QQ;
	}
	public String getUser_tel() {
		return user_tel;
	}
	public void setUser_tel(String user_tel) {
		this.user_tel = user_tel;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String user_password) {
		this.user_password = user_password;
	}
	public String getUser_realname() {
		return user_realname;
	}
	public void setUser_realname(String user_realname) {
		this.user_realname = user_realname;
	}
	@Override
	public String toString() {
		return "User [user_id=" + user_id + ", user_img=" + user_img
				+ ", user_nickname=" + user_nickname + ", user_sex=" + user_sex
				+ ", user_city=" + user_city + ", user_IDcard=" + user_IDcard
				+ ", user_add=" + user_add + ", user_postcode=" + user_postcode
				+ ", user_QQ=" + user_QQ + ", user_tel=" + user_tel
				+ ", user_password=" + user_password + ", user_realname="
				+ user_realname + "]";
	}
	
}
